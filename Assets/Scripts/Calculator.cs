using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public static class Calculator
{
    public static float GetCombinationValue(int valueMultiplier, float betValue)
    {
        return valueMultiplier * betValue;
    }

    public static float GetSpinCost(float valueMultiplier, float betValue)
    {
        return valueMultiplier * betValue;
    }
}
